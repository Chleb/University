    private void ListaWolneValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ListaWolneValueChanged
        int i = ListaWolne.getSelectedIndex();
        if(i >= 0)
        {
            if(selectedList == 1)
            {
            ListaAktywne.clearSelection();
            ListaZakonczone.clearSelection();
            selZglo = wolne[i];
            }
        }
        else
            selZglo = null;
        resetText();
    }//GEN-LAST:event_ListaWolneValueChanged

