    public void reset()
    {
        selZglo = null;
        try {
            wolne = DataControl.wolneZgl();
            aktywne = DataControl.aktywneZgl();
            zakonczone = DataControl.zakonczoneZgl();
        } catch (SQLException ex) {
            Logger.getLogger(Panel_Zgloszenia.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        ListaWolne.clearSelection();
        ListaAktywne.clearSelection();
        ListaZakonczone.clearSelection();
        DefaultListModel listModel = (DefaultListModel) ListaWolne.getModel();
        listModel.removeAllElements();
        for(int i = 0; i < wolne.length; i++)
        {
            listModel.addElement(wolne[i].auto);
        }
        listModel = (DefaultListModel) ListaAktywne.getModel();
        listModel.removeAllElements();
        for(int i = 0; i < aktywne.length; i++)
        {
            listModel.addElement(aktywne[i].auto);
        }
        listModel = (DefaultListModel) ListaZakonczone.getModel();
        listModel.removeAllElements();
        for(int i = 0; i < zakonczone.length; i++)
        {
            listModel.addElement(zakonczone[i].auto);
        }
        resetText();
    }
    
    public void resetText()
    {
        if(selZglo != null)
        {
            SWOpis.setText(selZglo.opis);
            SWKlient.setText(selZglo.klient);
            SWTel1.setText(selZglo.telnum.substring(0, 3));
            SWTel2.setText(selZglo.telnum.substring(4, 7));
            SWTel3.setText(selZglo.telnum.substring(8, 11));
            SWAuto.setText(selZglo.auto);
            SWDataZgl.setText(selZglo.data1);
            SWDataPrz.setText(selZglo.data2);
            SWDataZwr.setText(selZglo.data3);
        }
        else
        {
            SWOpis.setText("");
            SWKlient.setText("");
            SWTel1.setText("");
            SWTel2.setText("");
            SWTel1.setText("");
            SWAuto.setText("");
            SWDataZgl.setText("");
            SWDataPrz.setText("");
            SWDataZwr.setText("");
        }
    }

